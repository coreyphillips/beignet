/**
 * FFOR M4 — the Variant B tower `T` (spec §9.4).
 *
 * A standalone agent chosen by R that holds preimages hostage against valid
 * settlement packages, stores the packages, serves them back to R on
 * authenticated request, and watches for a revoked C_{n0}^S broadcast. It
 * holds NO funds and (except the scoped §9.4 option (a) revocation secret)
 * no channel keys.
 *
 * By design this module has ZERO coupling to ChannelManager/Channel: it is
 * provisioned with plain data, synthesizes its own read-only mirror of the
 * channel state, and talks to the world only through IFforTowerClient (the
 * transport is out of scope per the spec — an in-process loopback lives here
 * for tests, and example/ffor-tower.ts runs the same object standalone).
 *
 * NOTE (spec §9.4 provisioning erratum): the spec's provisioning list omits
 * several items the checklist cannot run without — the pre-epoch balances and
 * commitment numbers (deterministic C_i^R reconstruction), the full channel
 * configs (the §8 reserve/in-flight/slot checks), the channel_type, R's
 * channel role, and S's NODE id (the package signature is S's node-key sig
 * and is unverifiable without it). IFforTowerProvisioning carries the
 * superset.
 */

